    private void btnEseguiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEseguiActionPerformed
           String fileName = "auto.dat";
           ArrayList<Auto> list = Funzioni.read(fileName);
           if(radioModifica.isSelected()) //
           {
                if(!txtModello.getText().isEmpty() && !txtMarca.getText().isEmpty()) {
                    p.setMarca(txtMarca.getText());
                    p.setModello(txtModello.getText());   
                    p.setColore(Colore.valueOf(comboboxColore.getSelectedItem().toString()));
                    p.setAlimentazione(Alimentazione.valueOf(comboboxAlimentazione.getSelectedItem().toString()));       
                    Cilindrata cc = (Cilindrata)comboboxCilindrata.getSelectedItem(); // combobox di enum tiene oggetti di tipo enum
                    p.setCilindrata(cc);
             
                    if(checkboxTurbo.isSelected())
                        p.setIsTurbo(true);
                    else
                        p.setIsTurbo(false);
            
                    int index = Integer.parseInt(txtNumeroIndice.getText());
            
                    if(list.set(index, p) != null){
                    write(fileName, list);
           
                    JOptionPane.showMessageDialog(this, "Dati dell'auto:\n\n"+p.toString(),"Auto Modificata",JOptionPane.INFORMATION_MESSAGE);
                    JOptionPane.showMessageDialog(this, "Update completato!");   
                    }
                }
                else
                    JOptionPane.showMessageDialog(this, "Devi inserire Marca e Modello");
           
           }  
           else{ //Seguono azioni per cancellazione
               try{
               
               lblDBGIDX.setText(String.valueOf(index)); //componente di debug, attivare cambiando DEBUG su true
               index = Integer.parseInt(txtNumeroIndice.getText());
               lblDBGIDX.setText(String.valueOf(index));
                if(delete(fileName, list, index) != null){
                    JOptionPane.showMessageDialog(this, "Eliminazione completata!");
                    txtMarca.setText("");
                    txtModello.setText("");
                    checkboxTurbo.setSelected(false);
                    }
                else
                    JOptionPane.showMessageDialog(this, "Eliminazione non riuscita!");
               }catch(IndexOutOfBoundsException ex){
                    JOptionPane.showMessageDialog(this, index +" è fuori lista !");
               }catch(Exception ex){
                    JOptionPane.showMessageDialog(this, ex);
        }
           }        
    }//GEN-LAST:event_btnEseguiActionPerformed

