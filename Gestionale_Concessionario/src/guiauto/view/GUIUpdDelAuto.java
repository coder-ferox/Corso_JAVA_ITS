    private void btnCercaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCercaActionPerformed
        String fileName = "auto.dat";
        ArrayList<Auto> listaAuto = Funzioni.read(fileName);
        
        
        try{
        index = Integer.parseInt(txtNumeroIndice.getText());
        lblDBGIDX.setText(String.valueOf(index));
        p = searchByPosition(index, fileName);
        
            if(listaAuto != null) {
                
                txtMarca.setText(p.getMarca());
                txtModello.setText(p.getModello());
                if(p.isIsTurbo())
                    checkboxTurbo.setSelected(true);
                else
                    checkboxTurbo.setSelected(false);
                
                comboboxCilindrata.setSelectedItem(p.getCilindrata());
                comboboxAlimentazione.setSelectedItem(p.getAlimentazione());
                comboboxColore.setSelectedItem(p.getColore());
                ricercariuscita = true;
                
            }
        }catch(IndexOutOfBoundsException ex){
                JOptionPane.showMessageDialog(this, index + " è fuori lista !");
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex);
        }
    }//GEN-LAST:event_btnCercaActionPerformed

